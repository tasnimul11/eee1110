//arithmatic operators workflow using input from users

#include <iostream>
using namespace std;
int main()
{
    int m, n;
    cout<<"Input 2 values of m & n"<<endl;
    cout<<"m = " <<endl;
    cin>> m;
    cout<<"m = "<< m <<" & ";

    cout<<"n = " <<endl;
    cin>> n;
    cout<<"n = "<< n <<endl;

    cout<<"\t m+n = "<< m+n <<endl;
    cout<<"\t m*n = "<< m*n <<endl;
    cout<<"\t m-n = "<< m-n <<endl;
    cout<<"\t m/n = "<< m/n <<endl;
    cout<<"   Remainder = "<< m%n <<endl;

    return 0;

}
