#include<iostream>
using namespace std;
int main()
{
    cout<<"    ___   __  __ ____ ______  ______ _____ _____"<<endl;
    cout<<"   /   | / / / / ___/ _  __/ / ____/ ____/ ____/"<<endl;
    cout<<"  / /| |/ / / /\\\__ \\\  / /   / __/ / __/ / __/"<<endl;
    cout<<" / ___ / /_/ / __/ / / /   / /___/ /___/ /___"<<endl;
    cout<<"/_/  |_\\\____//___ / /_/   /_____/ ____/_____/"<<endl;

    /*THE TRICK IS If you want to print a backslash (\\), you need to use 3 backslashes (\\\) in your string.
     The first backslash escapes the second one, so a single backslash is printed*/
}
