#include<iostream>
using namespace std;
int main()
{
    float m, n;
    cout<<"Enter two values (Use SPACE to separate)"<<endl;
    cin>> m>> n;

    cout<<"Addition = "<< m+n<<endl;
    cout<<"Subtraction = "<< m-n<<endl;
    cout<<"Multiplication = "<< m*n<<endl;
    cout<<"division = "<< m/n<<endl;

    return 0;
}
